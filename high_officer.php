           <p id="clr">বাংলাদেশ রেলওয়ের ওয়েবসাইট www.railway.gov.bd এ আপনাদের স্বাগতম। বাংলাদেশ রেলওয়ের ওয়েব সাইটটি শুধুমাত্র রেলওয়ের যাত্রী এবং রেলপথে মালামাল পরিবহনকারী জনসাধারণের জন্য তৈরী করা হয়নি, বরং বাংলাদেশ রেলওয়ের সঙ্গে ওতপ্রোতভাবে জড়িত এবং রেলওয়ের ব্যাপারে আগ্রহী জনসাধারণ, সামাজিক ও বাণিজ্যিক প্রতিষ্ঠানসমূহের চাহিদার প্রতি লক্ষ্য রেখে বর্ধিত কলেবরে তা সাজানো হয়েছে। আশাকরি, নতুন এ ওয়েবসাইটটি রেল সংক্রান্ত দৈনন্দিন তথ্যাদি দিয়ে আপনাদের চাহিদা পূরণে সক্ষম হবে।বাংলাদেশ রেলওয়ের বিভিন্ন প্রয়োজনীয় তথ্যাদি যেমন ট্রেন চলাচলের খবরাখবর, এসএমএস এর মাধ্যমে ট্রেনের টিকেট ক্রয়, ট্রেনের ভাড়া ইত্যাদি এ ওয়েব পেজে প্রদর্শন করতে পেরে আমরা অত্যন্ত আনন্দিত ও গর্বিত। এ ছাড়াও রেলওয়ের গুরুত্বপূর্ণ অপারেশনাল ও বাণিজ্যিক তথ্যাদি এতে সন্নিবেশিত হয়েছে। অন্যান্য দেশের তুলনায় বাংলাদেশ রেলওয়ে তুলনামূলকভাবে একটি ছোট (২৮৭৭.১০ কি:মি:) রেলওয়ে নেটওয়ার্ক, যার মধ্যে ৬৫৯.৩৩ কি:মি: ব্রড গেজ, ১৮০৮.০৫ কি:মি: মিটার গেজ ও ৪০৯.৭২ কি:মি: ডুয়েল গেজ রেল লাইন বিদ্যমান। </P> 
