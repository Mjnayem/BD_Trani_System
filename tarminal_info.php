            <h2 id="divission">সিলেট টার্মিনাল এর তথ্য   </h2><br/>
          <p> সিলেট / sɪlhət / এছাড়া জালালাবাদ নামে পরিচিত, উত্তর-পূর্ব বাংলাদেশে একটি মেট্রোপলিটান শহর. সিলেট বিভাগের দপ্তর. শহর উত্তর-পূর্ব বাংলায় সুরমা নদীর ডান তীরে অবস্থিত. এটা একটি প্রায় ক্রান্তীয় জলবায়ু এবং মদ্যপ পার্বত্যাঞ্চল ভূখণ্ড রয়েছে. \ শহর বেশি 479.837 বাসিন্দাদের এর একটি জনসংখ্যা রয়েছে. [5] সিলেট বাংলাদেশের সবচেয়ে গুরুত্বপূর্ণ আধ্যাত্মিক ও সাংস্কৃতিক centers.It এক মাত্র ঢাকা ও চট্টগ্রামের পর বাংলাদেশের সবচেয়ে গুরুত্বপূর্ণ শহর এক কারণ অর্থনীতি, পর্যটন ইত্যাদি অনেক কারণে নগরীর দরগাহ প্রতি বছর কয়েক হাজার ভক্ত আঁকা হয়. <p/>
