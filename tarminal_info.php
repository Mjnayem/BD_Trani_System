          <p>চট্টগ্রাম বাংলা উচ্চারণ: [tʃɔt̠t̠ɔgram], Chatga) একটি প্রধান উপকূলবর্তী বন্দর শহর এবং দক্ষিণ-পূর্ব বাংলাদেশের আর্থিক কেন্দ্র. শহর অধিক 2.5 মিলিয়ন [1] যখন মেট্রোপলিটান এলাকার 6.5 মিলিয়ন ওভার এর একটি জনসংখ্যা রয়েছে, [5] এটি দেশের দ্বিতীয় বৃহত্তম শহর তৈরীর একটি জনসংখ্যা রয়েছে. এটা একটি eponymous জেলা ও বিভাগীয় রাজধানী. শহর পার্বত্য চট্টগ্রাম এবং বঙ্গোপসাগর মধ্যে কর্ণফুলী নদীর তীরে অবস্থিত. চট্টগ্রাম বিভাগের অনেক ভারত প্লেট এবং বার্মা প্লেটের সীমানা উপর পরিবেশগত ইন্দো-বার্মা জোন মধ্যে অবস্থিত. এই চট্টগ্রাম ভারতীয় উপমহাদেশ ও দক্ষিণ-পূর্ব এশিয়ার সংযোগস্থলে নির্মিত হয়. বন্দরনগরীর Chatigaon, Chatigam, Chattagrama, ইসলামাবাদ, চট্টলা, Chaityabhumi এবং পোর্ট গ্রান্ডে ডি বেঙ্গলা সহ ইতিহাসের বিভিন্ন নাম, দ্বারা পরিচিত হয়েছে.<p/>
          <p>

চট্টগ্রামের প্রাকৃতিক পোতাশ্রয় বঙ্গ অঞ্চলের একটি প্রাচীন প্রবেশদ্বার. এটা 1 ম শতাব্দীতে রোমান ভূগোলবিদ টলেমির বৃহত্তম Eastern পোর্ট এক হিসাবে উল্লেখ করা হয়েছে. বাঙালির সমৃদ্ধ সমুদ্রে ভ্রমণ ঐতিহ্যের অংশ হিসেবে উপকূলীয় চট্টগ্রাম বসতি স্থাপন করে এবং ভারতীয় ও বাংলা রাজ্যগুলির দ্বারা শাসিত হয়. আরব ব্যবসায়ীদের 9th শতকে চট্টগ্রামের সুগঠিত মুদ্রা, ব্যাংকিং এবং শিপিং দেখেছি. প্রারম্ভিক বিশ্বজনীন মুসলমানদের সামুদ্রিক বাণিজ্যের একজন আড়ত, [6] আরাকান, বাংলা ও ত্রিপুরার ব্যাপকতর পশ্চাদ্ভূমি নিয়ন্ত্রণের জন্য প্রতিযোগিতা যখন বন্দর ওপর আধিপত্য প্রতিষ্ঠা করেন. মুসলিম বিজয়ের 14 তম শতাব্দীর মধ্যে স্থান গ্রহণ. চট্টগ্রাম বাংলার সালতানাতের প্রধান বন্দর হয়ে ওঠে. ইবনে বতুতা এবং Niccolò ডি 'কন্টি সহ বিভিন্ন নেতৃস্থানীয় মধ্যযুগীয় বিশ্বব্যাপী অভিযাত্রী, দ্বারা ব্যবহৃত হয়. পরবর্তীতে ম্রাউক ইউ, পর্তুগিজ ট্রেডিং পোস্ট থেকে সহায়তা দিয়ে, এলাকার নিয়ন্ত্রণ প্রতিষ্ঠা করে. চট্টগ্রামের মোগল বিজয়ের বাংলা নিয়ন্ত্রণ পুনঃপ্রতিষ্ঠা এবং স্থিতিশীলতা ও বাণিজ্য একটি যুগের সূচনা করে. শহর ইসলামাবাদের নতুন নামকরণ করা হয়. এই বিচিত্র ইতিহাস বাংলা, যা প্রায় 50% আরবি বংশোদ্ভূত শব্দভান্ডার হয়েছে, সেইসাথে ফার্সি ও পর্তুগিজ ঋণকৃত শব্দসমূহ গ্রামীণ Chittagonian, উপভাষা প্রতিফলিত হয়. [7]

1760 সালে ব্রিটিশ ইস্ট ইন্ডিয়া কোম্পানিকে সমর্পিত, চট্টগ্রাম ব্রিটিশ রাজের অধীনে পূর্ববঙ্গ ও আসামের প্রধান বন্দর, সেইসাথে রেলপথ একটি হাব হয়ে ওঠে. একটি উল্লেখযোগ্য উপনিবেশবিরোধী বিদ্রোহ 1930 সালে সংঘটিত এটা দ্বিতীয় বিশ্বযুদ্ধে বার্মার প্রচারাভিযানের সময় মিত্রবাহিনীর জন্য একটি গুরুত্বপূর্ণ ঘাঁটি ছিল. দ্রুত শিল্পায়ন, যুদ্ধের অনুসৃত চট্টগ্রামকে পূর্ব পাকিস্তানের অংশ হয়ে ওঠে. 1971 সালে বাংলাদেশের স্বাধীনতা যুদ্ধের সময়, চট্টগ্রাম স্বাধীন দেশের ঘোষণা সাইট.<P/>
          <p>

আধুনিক চট্টগ্রাম দক্ষিণ এশিয়ার একটি গুরুত্বপূর্ণ অর্থনৈতিক কেন্দ্রবিন্দু. চট্টগ্রাম স্টক এক্সচেঞ্জ বাড়িতে এবং বাংলাদেশের প্রাচীনতম এবং বৃহত্তম কোম্পানি অনেক হয়. চট্টগ্রাম বন্দর বঙ্গোপসাগরের বৃহত্তম আন্তর্জাতিক সমুদ্র বন্দর. [8] এটা বাংলাদেশ নৌবাহিনীর বৃহত্তম বেস. তবে, একটি উচ্চ হচ্ছে এবং দক্ষিণ এশিয়া [9] ক্রমবর্ধমান শহুরে মাথাপিছু জিডিপি এবং বিশ্বের দ্রুততম ক্রমবর্ধমান শহরগুলোর অন্যতম হচ্ছে, সত্ত্বেও [10] এটি 11.3% এর একটি দারিদ্র্য হার, 2010. চট্টগ্রাম হিসাবে সুখ্যাতি রয়েছে একটি অপেক্ষাকৃত পরিচ্ছন্ন শহর, কিন্তু এখনও সারগর্ভ যৌক্তিক এবং আর্থ-সামাজিক সমস্যার মুখোমুখি. চট্টগ্রামের পাহাড়ী পশ্চাদ্ভূমি 2000 কবলিত উদ্ভিদ ও বিভিন্ন বিপন্ন বন্যপ্রাণী সঙ্গে বাংলাদেশের সবচেয়ে বৈচিত্রপূর্ণ অঞ্চল.

