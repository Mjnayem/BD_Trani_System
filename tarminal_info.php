সিলেটের নাম প্রাচীন ইন্দো-আর্য শব্দটি Srihatta এর ইংরেজ ফর্ম. [6] 1303 সালে সুফী মুসলমান নেতা শাহ জালাল স্থানীয় হিন্দু রাজা পরাজিত করে সিলেট জয় করেন. [7] ইবন বতুতা 14th শতাব্দীর মধ্যে সিলেট পরিদর্শন দেখল বাঙালি মুসলমানদের একটি কৃষি বাস্কেটও অঞ্চল রূপান্তর. [8] সিলেট বাংলায় সালতানাতের একটি টাকশাল শহর ছিল. 16 শতকের মধ্যে, সিলেট বারো-ভূঁইয়া জমিদারদের দ্বারা নিয়ন্ত্রিত ও মুঘল সাম্রাজ্যের একটি জেলা ছিল. [9] ব্রিটিশ শাসনের ইস্ট ইন্ডিয়া কোম্পানির শাসনাধীনে 18th শতাব্দীর মধ্যে শুরু হয়. তার প্রাচীন সমুদ্রে ভ্রমণ ঐতিহ্যের সঙ্গে, সিলেট ব্রিটিশ সাম্রাজ্যের lascars একটি প্রধান উৎস হয়ে ওঠে. সিলেট পৌর বোর্ড 1867 সালে প্রতিষ্ঠিত হয় [10] মূলত বেঙ্গল প্রেসিডেন্সির এবং পরে পূর্ববঙ্গ ও আসামের অংশ; শহরে 1874 এবং 1947 মধ্যে ঔপনিবেশিক আসামের অংশ ছিল, যখন একটি গণভোট এবং ব্রিটিশ ভারত বিভাগের নিম্নলিখিত, এটা পূর্ববাংলার অংশ হয়ে ওঠে. [7] সিলেট সিটি কর্পোরেশন 2001 সালে গঠন করা হয় বাংলাদেশ সরকার সিলেট 2009. একটি মেট্রোপলিটান এলাকার [11] মনোনীত বাংলাদেশের সবচেয়ে বৈচিত্রপূর্ণ অঞ্চল.

