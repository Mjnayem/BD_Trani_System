      <div class="content" id="clr">
          
        <p>বাংলাদেশ রেলপথ সরকারি মালীকানা ও সরকার কর্তৃক পরিচালিত দেশের একটি মুখ্য পরিবহন সংস্থা। মোট ২৫০৮৩ জন নিয়মিত কর্মচারীসহ বাংলাদেশ 
		রেলওয়ের মোট ২৮৭৭.১০ কি:মি: রুট রয়েছে। যেহেতু দেশের এক প্রান্তকে অন্য প্রান্তের সাথে সংযোজন করার জন্য রেলপথ একটি গুরুত্বপূর্ণ স্থল পরিবহন ব্যবস্থা,
		তাই রেলপথের সার্বিক উন্নতি দেশের অর্থনৈতিক উন্নতিতে গুরুত্বপূর্ণ ভূমিকা পালন করে।</p>
        <p>জুন ২, ১৯৮২ পর্যন্ত একজন চেয়ারম্যান ও চারজন মেম্বারসহ রেলওয়ে বোর্ডের নিকট রেলপথের ব্যবস্থাপনা ও উন্নয়ন ন্যস্ত ছিল। তারপর প্রশাসনিক 
		ও কার্যপরিচালনার সুবিধার্থে রেলওয়ে বোর্ডের বিলূপ্তি ৩ জুন ১৯৮২ সালে কার্যকর হয় এবং রেলওয়ে বোর্ডের কার্যক্রম যোগাযোগ মন্ত্রণালয়ের রেলওয়ে ডিভিশনের নিকট 
		ন্যস্ত হয় এবং উক্ত বিভাগের সচিব বাংলাদেশ রেলওয়ের মহাপরিচালকের দায়িত্ব পালন করে। একই উদ্দেশ্যে দুই মহাব্যবস্থাপকের প্রশাসনিক নিয়ন্ত্রণে বাংলাদেশ রেলওয়েকে পূর্ব ও 
		পশ্চিম দুই অঞ্চলে ভাগ করা হয়। দুই অঞ্চলের দুইজন মহাব্যবস্থাপক বাংলাদেশ রেলওয়ের মহাপরিচালকের নিকট দায়বদ্ধ হয়। পরবর্তীতে ১২ আগস্ট ১৯৯৫ সালে 
		রেলপথের দৈনন্দিন কার্যক্রম মন্ত্রণালয় থেকে আলাদা হয়ে রেলওয়ে প্রফেশনালদের নিয়ে মহাপরিচালকের হাতে ন্যস্ত হয়। নীতি নির্ধারণের জন্য যোগাযোগ মন্ত্রণালয়ের
		মন্ত্রীকে চেয়ারম্যান করে ৯ সদস্য বিশিষ্ট Bangladesh Railway Authority
		(BRA) গঠিত হয়। অতিরিক্ত মহাপরিচালক ও যুগ্ম মহাপরিচালকগণ সমস্ত প্রশাসনিক ও নীতি নির্ধারণের কাজ সম্পন্ন করেন।</p>
        <p>দুই জোনের মহাব্যবস্থাপককে সহায়তা করেন বিভিন্ন বিশেষায়িত দপ্তর, যারা কার্য পরিচালনা, রক্ষণাবেক্ষণ ও আর্থিক ব্যবস্থাপনার জন্য দায়িত্বশীল থাকেন। 
		প্রত্যেক জোন আবার দুইটি প্রধান কার্য পরিচালনা বিভাগে বিভক্ত। এই বিভাগগুলো বিভাগীয় রেলওয়ে ব্যবস্থাপক (DRM) এর অধীনে পরিচালিত হয় এবং সংস্থাপন,
		পরিবহন, বাণিজ্যিক, অর্থিক, যান্ত্রিক, ওয়ে এন্ড ওয়ার্কস, সংকেত ও টেলিযোগাযোগ, বৈদ্যুতিক, চিকিৎসা, নিরাপত্তা বাহিনীর মত বিভিন্ন বিশেষায়িত দপ্তরের বিভাগীয়
		কর্মকর্তারা তাকে সহায়তা করে থাকেন। এছাড়াও দুইজন বিভাগীয় তত্ত্বাবধায়ক (Divisional Superintendent) এর অধীনে পূর্বাঞ্চলের পাহাড়তলী ও 
		পশ্চিমাঞ্চলের সৈয়দপুর কারখানা (Workshop) 
		আছে। অধিকন্তু ব্রড গেজ ও মিটার গেজ লোকোমোটিভের জেনারেল ওভারহলিং এর জন্য পার্বতীপুর চীফ এক্সিকিউটিভের নিয়ন্ত্রণে একটি লোকোমোটিভ কারখানা আছে।</p>
        <p>বাংলাদেশ রেলওয়েতে রেক্টরের অধীনে ‘রেলওয়ে ট্রেনিং একাডেমী (RTA)’, প্রধান পরিকল্পনা কর্মকর্তার অধীনে ‘পরিকল্পনা কোষ’ (Planning Cell),
		প্রধান সরঞ্জাম নিয়ন্ত্রকের অধীনে ‘সরঞ্জাম শাখা’ (Stores Department), 
		দুই জোনের হিসাব ও অর্থনৈতিক ব্যবস্থাপনা কার্যক্রমের সমন্বয় ও পরামর্শের জন্য অতিরিক্ত মহাপরিচালক (অর্থ) এর অধীনে ‘হিসাব বিভাগ’(Accounts Department) আছে।</p>
        <p>ট্রেন পরিচালনার সাথে সম্পর্কিত বাংলাদেশ রেলপথের বিভিন্ন কার্যক্রম পরিদর্শন এবং রেলপথে পরিবহনের নিরাপত্তা নিশ্চিত করার জন্য বাংলাদেশ সরকার প্রথমে যোগাযোগ
		মন্ত্রণালয়ের অধীনে পৃথক রেলপথ বিভাগ ও পরে রেলপথ মন্ত্রণালয় গঠন করে।</p>
